package ex_01_Java_Basics;

public class UnaryAssignmentDemo {
    public static void main(String[] args) {

        int count = 5;

        count++;
        System.out.println(count);

        count--;
        System.out.println(count);

        count += 10;
        System.out.println(count);

        count *= 2;
        System.out.println(count);
    }
}
