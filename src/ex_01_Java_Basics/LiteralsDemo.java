package ex_01_Java_Basics;

public class LiteralsDemo {
    public static void main (String[] args)
    {
        int decimal = 100;        // decimal literal
        int binary = 0b1010;      // binary literal
        int hex = 0x1A;           // hexadecimal literal
        double pi = 3.14;         // floating literal
        char letter = 'J';        // character literal
        String text = "Java";     // string literal
        boolean flag = true;      // boolean literal

        System.out.println(decimal);
        System.out.println(binary);
        System.out.println(hex);
        System.out.println(pi);
        System.out.println(letter);
        System.out.println(text);
        System.out.println(flag);
    }
}
